-- CREATING DATABASE
CREATE DATABASE LIBRARY;
USE LIBRARY;

-- CREATING THE TABLES
-- 1. BRANCH TABLE
 CREATE TABLE BRANCH
 (
 BRANCH_NO INT AUTO_INCREMENT PRIMARY KEY,
 MANAGER_ID INT NOT NULL,
 BRANCH_ADDRESS VARCHAR (300),
 CONTACT_NO VARCHAR (20)
 );
 
 -- 2. EMPLOYEE TABLE
 CREATE TABLE EMPLOYEE
 (
 EMP_ID INT AUTO_INCREMENT PRIMARY KEY,
 EMP_NAME VARCHAR (100) NOT NULL,
 POSITION VARCHAR (100),
 SALARY DECIMAL (10,2),
 BRANCH_NO INT,
 FOREIGN KEY (BRANCH_NO) REFERENCES BRANCH(BRANCH_NO)
 );
 
 -- 3. BOOKS TABLE
 CREATE TABLE BOOKS
 (
 ISBN VARCHAR(20) PRIMARY KEY,
 BOOK_TITLE VARCHAR (250),
 CATEGORY VARCHAR (100),
 RENTAL_PRICE DECIMAL (10,2),
 STATUS VARCHAR (3) NOT NULL CHECK (STATUS IN ('YES', 'NO')),
 AUTHOR VARCHAR (100),
 PUBLISHER VARCHAR (200)
 );
 
 -- 4. CUSTOMER TABLE
 CREATE TABLE CUSTOMER 
 (
 CUSTOMER_ID INT AUTO_INCREMENT PRIMARY KEY,
 CUSTOMER_NAME VARCHAR (200),
 CUSTOMER_ADDRESS VARCHAR (300),
 REG_DATE DATE 
 );
 
 -- 5. ISSUE STATUS TABLE
 CREATE TABLE ISSUE_STATUS
 (
 ISSUE_ID INT AUTO_INCREMENT PRIMARY KEY,
 ISSUED_CUSTOMER INT,
 ISSUED_BOOK_NAME VARCHAR (300),
 ISSUED_DATE DATE,
 ISBN_BOOK VARCHAR (100),
 FOREIGN KEY (ISSUED_CUSTOMER) REFERENCES CUSTOMER (CUSTOMER_ID),
 FOREIGN KEY (ISBN_BOOK) REFERENCES BOOKS(ISBN)
 );
 
 -- 6. RETURN STATUS TABLE
 CREATE TABLE RETURN_STATUS
 (
 RETURN_ID INT PRIMARY KEY,
 RETURN_CUS INT,
 RETURN_BOOK_NAME VARCHAR (300),
 RETURN_DATE DATE,
 ISBN_BOOK2 VARCHAR (50),
FOREIGN KEY (RETURN_CUS) REFERENCES CUSTOMER (CUSTOMER_ID),
FOREIGN KEY (ISBN_BOOK2) REFERENCES BOOKS(ISBN)
 ); 
 

